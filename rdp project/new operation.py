#Sets do not repeat
#sets operation using union & intersection & difference &
A={10,20,30,40,50}
B={40,50,60}
#Union (U)
print("print the union ",A|B)
#2nd intersection(&)
print("\nprint the intersection ",A&B)
#difference(-)
print("\nPrint the Difference ",A-B)
#Symmertric Difference(^)
print("\nPrint the symmetric Difference",A^B)


